package package_first;

public class Loop {
    public static void main(String[] args) {

        int i = 5;

//        while
        while(i < 10){
            System.out.println(i);
            i++;
        }
//        for
        for(int j = 1; j <= 10; j+=2){
            System.out.println(j);
        }
//        do while
    }
}
